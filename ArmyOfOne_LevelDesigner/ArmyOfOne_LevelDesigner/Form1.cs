        private void confirmLevelButton_Click(object sender, EventArgs e)
        {
            //Create an array that saves the data of the created grid
            int[,] gridArray = new int[7, 10];

            //Sets each entry of the grid to 0 first
            //Creates 10 columns
            for(int i = 0; i < 10; i++)
            {
                //And creates 7 rows
                for(int j = 0; j < 7; j++)
                {
                    gridArray[j, i] = 0;
                }
            }

            //Change the values of the grid array depending on the grid the user made
            //Check Row 1
            #region
            if (Grid1A.BackColor == Color.Red)
            {
                gridArray[0, 0] = 1;
            }

            if (Grid1B.BackColor == Color.Red)
            {
                gridArray[0, 1] = 1;
            }

            if (Grid1C.BackColor == Color.Red)
            {
                gridArray[0, 2] = 1;
            }

            if (Grid1D.BackColor == Color.Red)
            {
                gridArray[0, 3] = 1;
            }

            if (Grid1E.BackColor == Color.Red)
            {
                gridArray[0, 4] = 1;
            }

            if (Grid1F.BackColor == Color.Red)
            {
                gridArray[0, 5] = 1;
            }

            if (Grid1G.BackColor == Color.Red)
            {
                gridArray[0, 6] = 1;
            }

            if (Grid1H.BackColor == Color.Red)
            {
                gridArray[0, 7] = 1;
            }

